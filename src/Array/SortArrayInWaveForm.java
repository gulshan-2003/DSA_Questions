package Array;

public class SortArrayInWaveForm {
    public static void main(String[] args) {
        int [] arr ={1,2,3,4,5};
//        int[] newA = new int[arr.length];

        for(int i=0; i<arr.length-1; i+=2)
        {
            int temp = arr[i];
            arr[i] = arr[i+1];
            arr[i+1] = temp;
        }

        for(int a : arr)
        {
            System.out.println(a);
        }
    }
}
